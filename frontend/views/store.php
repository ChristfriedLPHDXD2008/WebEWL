<section id="fair-trade">
	<h3>Fair Trade nach den Konventionen der Weltläden</h3>
	<p>
		Unser Weltladen wird ehrenamtlich betrieben und arbeitet nach der Konvention der Weltläden.
		<br>Klicken Sie auf einen der Standards, um eine Zusammenfassung zu sehen:
	</p>
	<div class="wl-konvention">
		<h4><a href="#"><b><span>Standard <span class="wl-konv-number">1:</span></span></b> Handelspraktiken</a></h4>
		<div class="wl-konv-text">
			<p>
				"Alle am Fairen Handel beteiligten Akteure kooperieren miteinander und vermeiden unfairen
				Wettbewerb. Sie maximieren nicht ihren Gewinn auf Kosten anderer. Sie streben nach der
				Vergrößerung ihres Handelsvolumens, um eine Einkommenssteigerung und einen Zugewinn an
				sozialer Sicherheit für die Produzent/innen zu erreichen. [...]"
				<br>
			</p>
		</div>
	</div>
	<div class="wl-konvention">
		<h4><a href="#"><b><span>Standard <span class="wl-konv-number">2:</span></span></b> Arbeitsbedingungen</a></h4>
		<div class="wl-konv-text">
			<p>
				"Im gesamten Fairen Handel stehen die Menschen mit ihren elementaren Bedürfnissen im Vordergrund. [...]"
			</p>
			<span>Dies bezieht sich im Besonderen auf:</span>
			<ul>
				<li>Ausschluss von Diskriminierung,</li>
				<li>Ausschluss von ausbeuerischer Kinderarbeit,</li>
				<li>Ausschluss von Zwangsarbeit und</li>
				<li>das Recht auf Gewerkschaftsfreiheit.</li>
			</ul>
		</div>
	</div>
	<div class="wl-konvention">
		<h4><a href="#"><b><span>Standard <span class="wl-konv-number">3:</span></span></b> Transparenz</a></h4>
		<div class="wl-konv-text">
			<p>
				"Eine Grundlage des Fairen Handels der Weltläden ist Transparenz, sowohl gegenüber der
				Öffentlichkeit als auch zwischen den Handelspartner/innen.
				<br>Produzent/innen, Importorganisationen und Weltläden ermöglichen für ihre Handelspartner/innen
				eine Einsicht in ihre Zielsetzungen, ihre Organisationsstruktur, ihre Prozesse zur
				Entscheidungsfindung, ihre Besitzverhältnisse, ihre finanzielle Situation, ihre Handelswege und
				ihre Handelskriterien. [...]"
			</p>
		</div>
	</div>
	<div class="wl-konvention">
		<h4><a href="#"><b><span>Standard <span class="wl-konv-number">4:</span></span></b> Bildungs- und Informationsarbeit</a></h4>
		<div class="wl-konv-text">
			<p>
				"Die Akteure des Fairen Handels vermitteln Informationen über die Produkte, die Produzent/innen,
				die Herkunftsländer der Produkte, den Weg des Produkts zu den Verbraucher/innen und die
				Rahmenbedingungen und Machtverhältnisse im Welthandel.
				<br>Sie fördern die Entwicklung von beruflichen Kompetenzen ihrer Mitarbeiter/innen und bieten
				ihnen nach Möglichkeit Weiterbildungsmaßnahmen an. [...]"
			</p>
		</div>
	</div>
	<div class="wl-konvention">
		<h4><a href="#"><b><span>Standard <span class="wl-konv-number">5:</span></span></b> Öffentlichkeitsarbeit und politische Arbeit</a></h4>
		<div class="wl-konv-text">
			<p>
				"Die Akteure des Fairen Handels fördern über Öffentlichkeitsarbeit und Kampagnen das Bewusstsein über
				ihre politische Zielsetzungen sowie die Notwendigkeit, sich für Gerechtigkeit im Welthandel einzusetzen.
				<br>Durch die Darstellung in der Öffentlichkeit und in der Werbung werden die Ziele des Fairen Handels
				im Sinne dieser Konvention vermittelt."
			</p>
		</div>
	</div>
	<div class="wl-konvention">
		<h4><a href="#"><b><span>Standard <span class="wl-konv-number">6:</span></span></b> Umweltschutz</a></h4>
		<div class="wl-konv-text">
			<p>
				"Alle Aktuere des Fairen Handels bemühen sich um grötmögliche Umweltverträglichkeit im Herstellungs-
				und Vermarktungsprozess. [...]"
			</p>
		</div>
	</div>
	<div class="wl-konvention">
		<h4><a href="#"><b>Standard <span class="wl-konv-number">7:</span></b> Ergänzungsprodukte</a></h4>
		<div class="wl-konv-text">
			<p>
				"Das zentrale Anliegen der Weltläden ist die Förderung des Fairen Handels. Produkte aus Fairem Handel
				machen deshalb den überwiegenden Teil des Sortiments aus und prägen das Erscheinungsbild des Ladens. [...]"
			</p>
		</div>
	</div>
