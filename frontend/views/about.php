<section>
	<h3>Verein und Weltladen</h3>
	<p>
		Im Dezember 1990 gründeten Mitglieder der Franziskusgemeinschaft Leutwitz zusammen mit Freunden und Bekannten den Verein "Pax et bonum" - Eine Welt Laden e.V.
	</p>
	<p>
		Mit dem Verkauf von Waren aus Fairem Handel, der Unterstützung von Projekten in Entwicklungsländern und Info-Veranstaltungen in und um Bischofswerda will die Gruppe auf die ungerechte Welthandelssituation aufmerksam machen.
	</p>
	<p>
		Zum Vereinsprogramm gehören auch Seminare, Vorträge und Ausstellungen. Unterstützt von einer Bildungsreferentin bietet der Verein Projekttage für Kinder und Erwachsene zu entwicklungspolitischen Themen an.
	</p>
	<p>
		Aktivitäten rund um Verein und Laden werden von den Mitgliedern ehrenamtlich durchgeführt. Der Verein möchte mit seiner Arbeit einen kleinen Beitrag zu einer gerechteren Verteilung in der Welt leisten.
	</p>
	<hr>
</section>
<section>
	<h3>Was ist Fairer Handel?</h3>
	<p>
		Anfang der 1970er Jahre ist aus der Kritik an ungerechten Welthandelsstrukturen die "Aktion Dritte-Welt-Handel" entstanden. Die heute ca. 800 deutschen Weltläden verstehen sich als Teil dieser Bewegung.
	</p>
	<p>
		<em>Es wurden folgende Kriterien für den Fairen Handel festgelegt:</em>
	</p>
	<ul>
		<li>Sozial- und Umweltverträglichkeit</li>
		<li>umfassende Transparenz der Strukturen</li>
		<li>Mitbestimmung in den Fair-Handels-Organisationen</li>
		<li>Non-Profit</li>
		<li>Informations- und Bildungsarbeit</li>
		<li>Kontinuität der Handelsbeziehungen</li>
	</ul>
